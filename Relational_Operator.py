n1 = int(input("Enter first number: "))
n2 = int(input("Enter second number: "))

res = n1>n2
print("Greater than: ", res)
print("---------")

res = n1<n2
print("Less than: ", res)
print("---------")

res = n1>=n2
print("Greater than or equal to: ", res)
print("---------")

res = n1<=n2
print("Less than or equal to: ", res)
print("---------")

